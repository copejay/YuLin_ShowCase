type NameType = "human" | "noHuman";

export function randomInt(min: number, max: number): number {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function randomPick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function AutoName(type: NameType): string {
  if (type === "human") {
    const familyName = randomPick(humanFamilyName);

    // 人族名：1 或 2 字
    const givenNameLength = randomInt(1, 2);
    let givenName = "";

    for (let i = 0; i < givenNameLength; i++) {
      givenName += randomPick(humanGivenName);
    }

    return familyName + givenName;
  }

  // 非人族：1～4 字，自由组合
  const nameLength = randomInt(1, 4);
  let name = "";

  for (let i = 0; i < nameLength; i++) {
    // 姓名混合，增强异族感
    const source =
      Math.random() < 0.5 ? yaoFamilyName : yaoGivenName;
    name += randomPick(source);
  }

  return name;
}